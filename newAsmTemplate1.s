PROCESSOR 16F887
#include <xc.inc>
CONFIG FOSC=INTRC_NOCLKOUT
CONFIG WDTE=OFF
CONFIG PWRTE=ON
CONFIG MCLRE=OFF
CONFIG CP=OFF
CONFIG CPD=OFF
CONFIG BOREN=OFF
CONFIG IESO=OFF
CONFIG FCMEN=ON
CONFIG DEBUG=ON

CONFIG BOR4V=BOR40V
CONFIG WRT=OFF


PSECT resetVec,class=CODE,delta=2
resetVec:
PAGESEL LUZOSC
goto LUZOSC
                        
LUZOSC:
    BANKSEL ANSEL
    clrf ANSEL
    BANKSEL PORTA
    clrf PORTA
    BANKSEL TRISA
    clrf TRISA
    BANKSEL PORTA
    
    SEC1:
    rrf PORTA,1
    
    btfss PORTA,0
    goto SEC1
    SEC2:
    rlf PORTA,1
  
    btfss PORTA,7
    goto SEC2
    goto SEC1
    END